package GameLogic;

/**
 * Enum that represents the current state of the game
 * <br> Menu - A menu is being displayed
 * <br>Running - The game is Running (play)
 * <br>Pause - The game is paused
 * <br>Lose - The game was Lost
 * <br> Win - The game was Won
 */

public enum EnumGameState {
    Menu,
    Running,
    Pause,
    Lose,
    Win
}
